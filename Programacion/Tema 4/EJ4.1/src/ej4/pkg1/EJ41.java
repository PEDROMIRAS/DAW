package ej4.pkg1;
/**
 * Diseñar una funcion eco() qa la que se le pase parametro un numero n y muestra por pantalla n veces Eco...
 */
/**
 *
 * @author pedromiras
 */
public class EJ41 {

    /**
     * @param args the command line arguments
     */
    static void eco(int n){
        for (int i = 0; i < n; i++) {
            System.out.println("Eco...");
        }
    }
    public static void main(String[] args) {  
      eco(79);
    }
    
}
